function LetterChanges(str) { 
  var result='';
  var allAlpha=['a','b','c','d','e','f','g','h','i','j','k','l','m','n',
  'o','p','q','r','s','t','u','v','w','x','y','z'];
  for (var i = 0; i < str.length; i++) {
    if (allAlpha.indexOf(str[i])!==-1) {
      if (str[i]==='z' ||str[i]==='d' ||str[i]==='h' ||str[i]==='n' ||str[i]==='t' ) {
        if (str[i]==='z') {
          result+=allAlpha[0].toUpperCase();
        }else{
          result+=allAlpha[allAlpha.indexOf(str[i])+1].toUpperCase();
        }
      }else{
        result+=allAlpha[allAlpha.indexOf(str[i])+1]; 
      }
    }else{
      result+=str[i]; 
    }
  }  
  // code goes here  
  return result;        
}                                       
